// Dekrementacja - zmniejszenie wartości zmiennej lub numeru o 1,

//     Inkrementacja - zwiększenie wartości zmiennej lub numeru o 1,

// Przykład 1 -
// inkrementacja dodająca++ po wartości, najłatwiej to zrozumieć w ten sposób, że jeśli mam x++ to do zmiennej x dodajemy 1
// ale dodaje się to dopiero w kolejnej linii,
let x = 5;
console.log(x++); //5
console.log(x); //6

// Przykład 2
//  inkrementacja dodająca ++ przed wartością, w tym rodzaju inkrementacji dodajemy 1 w tej samej linii co zapisanie tej inkrementacji,
let x = 5;
console.log(++x); //6
console.log(x); //6

// Dwa sposoby działania i zapisu DEKREMENTACJI:

// Przykład 1
// dekrementacja z -- po wartości, najłatwiej to zrozumieć w ten sposób, że jeśli mam
// x-- to od zmiennej x odejmujemy 1 ale odejmuje się to dopiero w kolejnej linii,
let x = 5;
console.log(x--); //5
console.log(x); //4

//Przykład 2
// dekrementacja z -- przed wartością, w tym rodzaju dekrementacji odejmujemy 1 w tej samej linii co zapisanie tej dekrementacji,
let x = 5;
console.log(--x); //4
console.log(x); //4
